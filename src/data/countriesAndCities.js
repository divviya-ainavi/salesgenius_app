// Get list of countries
export const getCountries = () => {
  return Object.keys(countriesAndCities).sort();
};

// Get cities for a specific country
export const getCitiesForCountry = (country) => {
  return countriesAndCities[country] || [];
};

// Validate if a country exists
export const isValidCountry = (country) => {
  return Object.keys(countriesAndCities).includes(country);
};

// Validate if a city exists for a given country
export const isValidCityForCountry = (country, city) => {
  const cities = getCitiesForCountry(country);
  return cities.includes(city);
};